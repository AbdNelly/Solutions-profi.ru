def RemoveDuplicates(L):
  S = set()
  C = []
  for elem in L:
    if elem in S: 
      continue
    S.add(elem)
    C.append(elem)
  return C

"""Какая асимптотическая сложность у этой функций?

O(n) в большинстве случаев, поскольку в списке n элементов (мы пройдемся по нему один раз), set реализован через hash tables (вставка O(1) в среднем), для списка append O(1).
Этот способ более хорош по времени, но более плох по памяти.
Подходит для случаев, когда дубликатов действительно много.

Также можно было бы реализовать этот алгоритм с использованием сортировки и одному проходу по списку после (O(nlogn) + O(n) = O(nlogn)). Этот способ хуже по времени, зато лучше по памяти. Подходит для случаев, когда список очень большой и мало дубликатов.
"""